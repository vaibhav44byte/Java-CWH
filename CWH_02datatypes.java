package com.company;

public class CWH_02datatypes {
    public static void main(String[] args) {
        int a = 23 + 3;
        float b = 3.56f + 7;
        byte c = 6;
        // increment and decrement
        int i = 56;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);
        int y = 7;
        System.out.println(++y * 8);
    }
}
