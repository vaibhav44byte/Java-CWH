package com.company;

public class CWH_10RL_operators {
    public static void main(String[] args) {
        // AND operator
        boolean a = true;
        boolean b = false;
        if (a && b) {
            System.out.println("Y");
        } else {
            System.out.println("N");
        }
        // OR operator

        if (a || b) {
            System.out.println("Y");
        } else {
            System.out.println("N");
        }
        // NOT operator

        // True become false, false become true
    }
}
